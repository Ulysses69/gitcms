<div class="box">
<h2>About</h2>
<p>Tokens are bits of content that can be placed into pages via small placeholders (shortcuts), like [email] or [address]. Once placed within pages, all instances of a token placeholder can be changed simultaneously by changing the literal value associated with it on this page.</p>
</div>

<div class="box">
<h2>Usage</h2>
<p>To create a token, a simple word should be wrapped within square brackets like [telephone] for example, to act as the name of the token placeholder. Each token then needs a literal value associated with it, such as an actual telephone number such as 01242 236600. An equivalent value can also be used in place of main value, such as when called by mobile instances (optional).</p>
</div>