		$encoded_clientaddress = str_replace('++','+',$encoded_clientaddress);
		$encoded_clientaddress = str_replace('&','%26',$encoded_clientaddress);
		$goaddress = 'http://maps.google.com/maps/api/geocode/json?address='.$encoded_clientaddress.'&sensor=false';
		$geocode = file_get_contents($goaddress);
		$output = json_decode($geocode);
		if(is_object($output)){
			$latitude = $output->results[0]->geometry->location->lat;
			$longitude = $output->results[0]->geometry->location->lng;
